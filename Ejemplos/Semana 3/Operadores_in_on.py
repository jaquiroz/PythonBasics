#in permite buscar un sub string dentro del string total
print("Ejercicios Operador in")
print('p' in 'apple')
print('i' in 'apple')
print('ap' in 'apple')
print('pa' in 'apple')

print('a' in 'a')
print('apple' in 'apple')
print('' in 'a')
print('' in 'apple')

#not in busca un sub string que no esta dentro del string total
print("Ejercicios Operador in")
print('x' not in 'apple')

#Aplicacion en Listas
print("Aplicacion en listas")
print("a" in ["a", "b", "c", "d"])
print(9 in [3, 2, 9, 10, 9.0])
print('wow' not in ['gee wiz', 'gosh golly', 'wow', 'amazing'])


